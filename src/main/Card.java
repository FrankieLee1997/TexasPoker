public class Card {
    private Integer num;
    private Character suit;

    //Constructor
    public Card() {
        this.num = num;
        this.suit = suit;
    }

    //Getter and Setter
    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public Character getSuit() {
        return suit;
    }

    public void setSuit(Character suit) {
        this.suit = suit;
    }

}
